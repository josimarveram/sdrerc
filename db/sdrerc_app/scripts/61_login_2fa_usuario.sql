DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(1) INTO v_count
    FROM user_tables
   WHERE table_name = 'USUARIO_TOTP_BACKUP_CODE';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE q'[
      CREATE TABLE usuario_totp_backup_code (
        id_backup_code NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
        id_usuario NUMBER NOT NULL,
        codigo_hash VARCHAR2(250) NOT NULL,
        usado NUMBER(1) DEFAULT 0 NOT NULL,
        usado_en TIMESTAMP,
        creado_en TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
      )
    ]';
  END IF;
END;


DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(1) INTO v_count
    FROM user_constraints
   WHERE table_name = 'USUARIO_TOTP_BACKUP_CODE'
     AND constraint_name = 'FK_USR_TOTP_BACKUP_USUARIO';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE q'[
      ALTER TABLE usuario_totp_backup_code
      ADD CONSTRAINT fk_usr_totp_backup_usuario
      FOREIGN KEY (id_usuario) REFERENCES usuario (id_usuario)
    ]';
  END IF;
END;


