/* ============================================================
   SCRIPT 61 - Soporte de esquema para Login premium con 2FA (TOTP)
   Ejecutar conectado como SDRERC_APP.

   Contexto: hoy la columna usuario.password_hash existe pero nunca se
   usa (UsuarioDAO.insertarUsuario la fija a NULL, actualizarUsuario ni
   siquiera la actualiza) y MainV2 abre MenuPrincipalV2 sin ningun
   login. Este script agrega el soporte de esquema necesario para:

   - Forzar cambio de contraseña en el primer login tras un
     "Restablecer clave" del administrador (debe_cambiar_password).
   - Enrolamiento y verificacion TOTP (RFC 6238) por usuario
     (totp_secret cifrado en la capa de aplicacion con AES-GCM, nunca
     en claro; totp_habilitado; totp_confirmado_en).
   - Bloqueo temporal tras intentos fallidos de password o de codigo
     TOTP (intentos_fallidos, bloqueado_hasta).
   - Auditoria minima (password_actualizado_en, ultimo_login_en).
   - Codigos de respaldo de un solo uso para cuando se pierde el
     dispositivo autenticador (tabla usuario_totp_backup_code, mismo
     patron de hash que password_hash: BCrypt via PasswordEncoder).

   No se toca ninguna tabla ni columna legacy (APP_USERS, etc.). No
   se toca OracleConnection.java ni el flujo de login legacy.

   Idempotente en su totalidad (verifica user_tab_columns/user_tables
   antes de cada ALTER/CREATE).
   ============================================================ */

DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(1) INTO v_count
    FROM user_tab_columns
   WHERE table_name = 'USUARIO' AND column_name = 'DEBE_CAMBIAR_PASSWORD';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE 'ALTER TABLE usuario ADD (debe_cambiar_password NUMBER(1) DEFAULT 0 NOT NULL)';
  END IF;
END;


